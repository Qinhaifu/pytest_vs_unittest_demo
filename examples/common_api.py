def gram_to_kilogram(gram):
    """
    Converts gram to kilogram

    Args:
        gram (float): gram

    Returns:
        float: Kilogram

    """
    kilogram = gram / 1000
    return kilogram


def kilogram_to_gram(kilogram):
    """
    Converts kilometers to miles

    Args:
        kilogram (float): Kilogram

    Returns:
        float: gram

    """
    gram = kilogram * 1000
    return gram
